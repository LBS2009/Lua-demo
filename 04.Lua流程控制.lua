-- if 语句
a = -10
if (a < 0) then
    print("a < 0 ")
end

-- if else 语句
b = 10
if (b < 0) then
    print("b < 0")
else
    print("b >= 0")
end

-- if elseif else 语句
c = 10
if (c < 0) then
    print("c < 0")
elseif (c > 0) then
    print("c > 0")
else
    print("c = 0")
end